////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export type SpaceBarProps = { height: number; }

/**
 * @param props - Component Props
 * @see {@link SpaceBarProps}
 * @returns JSX
 */
export function SpaceBarPX({ height }: SpaceBarProps): JSX.Element {

  // Return Content
  return (
    <div style={{
      width: "100%",
      height: `${height}px`
    }}>
    </div>
  )
}

