import styles from "./index.module.scss";

import { AnchorHTMLAttributes } from "react";

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export type AnchorButtonProps = AnchorHTMLAttributes<HTMLAnchorElement>

/**
 * @param props
 * @see {@link AnchorButtonProps}
 * @returns JSX
 */
export function AnchorButton({ children, ...attributes }: AnchorButtonProps): JSX.Element {

  // Return Component
  return (
    <a className={styles.anchor} {...attributes}>
      <span className={styles.text}>{children}</span>
    </a>
  );
}

