import { ProjectKey } from "@/app/data/enums/ProjectKey";

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export function isUnknownValueProjectKey(value: unknown): value is ProjectKey {
  return typeof value === "string" && Object.values(ProjectKey).includes(value as ProjectKey);
}

export function isStringProjectKey(value: string): value is ProjectKey {
  return Object.values(ProjectKey).includes(value as ProjectKey);
}

