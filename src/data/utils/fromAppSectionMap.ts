////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import { AppSectionKey } from "../keys/AppSectionKey";
import { APP_SECTION_MAP } from "../maps/AppSectionMap";
import { AppSectionData } from "../types/AppSectionData";

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export function toAppSectionData(appSectionKey: AppSectionKey): AppSectionData {
  return APP_SECTION_MAP[appSectionKey];
}

export function toAppSectionDataArray(appSectionKeyArray: AppSectionKey[]): AppSectionData[] {
  return appSectionKeyArray.map((appSectionKey) => (APP_SECTION_MAP[appSectionKey]));
}

